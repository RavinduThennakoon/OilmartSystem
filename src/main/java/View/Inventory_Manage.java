private void populateTable(List<Item> items) {
    DefaultTableModel model = (DefaultTableModel) table_inventory.getModel();
    model.setRowCount(0); // clear existing rows

    for (Item item : items) {
        model.addRow(new Object[] {
            item.getItemCode(),
            item.getItemName(),
            item.getBrand(),
            item.getCategory(),
            item.getVehicleType(),
            item.getViscosityGrade(),
            item.getApiStandard(),
            item.getVolumn(),
            item.getUnit(),
            item.getPackageType(),
            item.getBatchNo(),
            item.getExpiryDate(),
            item.getCostPrice(),
            item.getSellingPrice(),
            item.getDiscount(),
            item.getStockQty(),
            item.getSupplier()
        });
    }
}

